def _macro_f1_counts(tp: list[int], fp: list[int], fn: list[int]) -> float:
    """根据每类 TP/FP/FN 直接计算 macro-F1。"""

    k = len(tp)
    if k <= 0:
        return 0.0
    s = 0.0
    for i in range(k):
        denom = 2 * tp[i] + fp[i] + fn[i]
        s += (2 * tp[i] / denom) if denom > 0 else 0.0
    return float(s / k)


def _ccc(x: torch.Tensor, y: torch.Tensor) -> float:
    """计算连续强度回归的 CCC 指标。"""

    x = x.flatten().to(torch.float64)
    y = y.flatten().to(torch.float64)
    n = int(x.numel())
    if n < 2:
        return 0.0
    mx = float(x.mean().item())
    my = float(y.mean().item())
    vx = float(x.var(unbiased=False).item())
    vy = float(y.var(unbiased=False).item())
    cov = float(((x - mx) * (y - my)).mean().item())
    denom = vx + vy + (mx - my) ** 2
    if denom <= 0:
        return 0.0
    return float((2.0 * cov) / denom)


