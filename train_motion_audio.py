def _print_split_label_hist(name: str, ds: CachedMotionAudioDataset, indices: list[int], label_names: list[str]) -> None:
    """打印某个子集的标签直方图，便于快速检查切分是否异常。"""

    num_classes = len(label_names)
    counts = torch.zeros(num_classes, dtype=torch.long)
    oob: list[tuple[int, int]] = []
    for i in indices:
        y = ds.samples[i].get("label", None)
        if isinstance(y, torch.Tensor):
            y_int = int(y.item())
        else:
            if y is None:
                oob.append((i, -1))
                continue
            y_int = int(y)
        if 0 <= y_int < num_classes:
            counts[y_int] += 1
        else:
            oob.append((i, y_int))
    total = int(counts.sum().item())
    parts = [f"{label_names[k]}:{int(v)}" for k, v in enumerate(counts.tolist())]
    print(f"{name} label histogram (n={total}): " + " ".join(parts))
    if oob:
        shown = ", ".join([f"idx={i}/label={y}" for i, y in oob[:10]])
        print(f"WARNING: {name} has out-of-range labels (show up to 10): {shown}")


def _prepare_dataset_for_task(ds: CachedMotionAudioDataset, task_mode: str, speaker_id: str | None) -> list[str]:
    """把缓存样本标签映射成当前任务模式下的局部标签空间。"""

    label_names = resolve_task_label_names(task_mode, speaker_id)
    for idx, sample in enumerate(ds.samples):
        raw_label = sample.get("label", None)
        raw_label_idx: int | None = None
        if isinstance(raw_label, torch.Tensor):
            raw_label_idx = int(raw_label.item())
        elif raw_label is not None:
            raw_label_idx = int(raw_label)
        global_label_en = EMOTIONS[raw_label_idx] if raw_label_idx is not None and 0 <= raw_label_idx < len(EMOTIONS) else None
        sample["_global_label_en"] = global_label_en
        task_label_idx = map_label_to_task_index(global_label_en, task_mode, speaker_id)
        if task_label_idx is None:
            continue
        if isinstance(raw_label, torch.Tensor):
            sample["label"] = torch.tensor(task_label_idx, dtype=torch.long)
        else:
            sample["label"] = int(task_label_idx)
    return label_names


def _infer_cached_embedding_dims(samples: list[dict[str, Any]]) -> dict[str, int]:
    """从缓存样本里探测 embedding 维度，用于跳过已冻结编码器的初始化。"""

    dims: dict[str, int] = {}
    for key in ("audio_emb", "text_emb", "rgb_emb", "flow_emb"):
        for sample in samples:
            value = sample.get(key, None)
            if isinstance(value, torch.Tensor) and value.ndim >= 1:
                dims[key] = int(value.shape[-1])
                break
    return dims


