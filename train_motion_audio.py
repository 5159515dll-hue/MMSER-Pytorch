def _autocast_context(device: torch.device, amp_mode: str) -> contextlib.AbstractContextManager[Any]:
    """根据解析后的 AMP 模式构造 autocast 上下文。"""

    if device.type != "cuda" or amp_mode == "off":
        return contextlib.nullcontext()
    amp_mod = getattr(torch, "amp", None)
    amp_dtype = torch.bfloat16 if amp_mode == "bf16" else torch.float16
    if amp_mod is not None and hasattr(amp_mod, "autocast"):
        return amp_mod.autocast("cuda", enabled=True, dtype=amp_dtype)  # type: ignore[attr-defined]
    return torch.cuda.amp.autocast(enabled=True)


def _cache_text_tokens(ds: CachedMotionAudioDataset, tokenizer: Any, max_text_len: int, *, text_policy: str) -> None:
    """预先把整集文本分词，避免每个 epoch / batch 重复 tokenizer。"""

    if not ds.samples:
        return
    if all("text_emb" in s for s in ds.samples):
        return
    if all("_text_input_ids" in s and "_text_attention_mask" in s for s in ds.samples):
        return
    texts = [
        select_text_for_policy(
            full_text=str(s.get("mn", "")),
            masked_text=str(s.get("masked_mn", "")),
            label_en=str(s.get("_global_label_en", "")) or None,
            policy=text_policy,
        )
        for s in ds.samples
    ]
    enc = tokenizer(
        texts,
        padding="max_length",
        truncation=True,
        max_length=int(max_text_len),
        return_tensors="pt",
    )
    input_ids = enc["input_ids"].to(torch.long)
    attention_mask = enc["attention_mask"].to(torch.long)
    token_type_ids = enc.get("token_type_ids", None)
    for idx, sample in enumerate(ds.samples):
        sample["_text_input_ids"] = input_ids[idx].clone()
        sample["_text_attention_mask"] = attention_mask[idx].clone()
        if token_type_ids is not None:
            sample["_text_token_type_ids"] = token_type_ids[idx].to(torch.long).clone()


def _infer_text_dim(model_name: str) -> int:
    """根据常见文本模型名估计 hidden size，供跳过文本编码器初始化时使用。"""

    name = str(model_name).lower()
    if "large" in name:
        return 1024
    return 768


def _infer_audio_dim(model_name: str) -> int:
    """根据音频编码器类型估计 mean+std 池化后的输出维度。"""

    name = str(model_name).lower()
    if name == "wav2vec2_base":
        return 2 * 768
    if "large" in name:
        return 2 * 1024
    return 2 * 768


def _validate_cache_compatibility(ds: CachedMotionAudioDataset, args: argparse.Namespace) -> None:
    """提前检查 cache 是否满足当前训练配置。"""

    sample = ds[0]
    video_backbone = str(args.video_backbone)
    text_policy = resolve_text_policy(getattr(args, "text_policy", "full"))
    feature_cfg = ds.config.get("feature_cache", {}) if isinstance(ds.config, dict) else {}
    cached_text_policy = str(feature_cfg.get("text_policy", "") or "").strip().lower()

    if video_backbone == "dual":
        if bool(args.freeze_flow):
            if "flow_emb" not in sample and "flow" not in sample:
                raise RuntimeError("Training requires flow_emb or flow in cache, but neither is present.")
        elif "flow" not in sample:
            raise RuntimeError("Current config trains flow branch, but cache has no raw 'flow'. Build raw cache or freeze flow.")
        if bool(args.freeze_rgb):
            if "rgb_emb" not in sample and "rgb" not in sample:
                raise RuntimeError("Training requires rgb_emb or rgb in cache, but neither is present.")
        elif "rgb" not in sample:
            raise RuntimeError("Current config trains RGB branch, but cache has no raw 'rgb'. Build raw cache or freeze rgb.")
    elif video_backbone == "videomae":
        if bool(args.freeze_rgb):
            if "rgb_emb" not in sample and "rgb" not in sample:
                raise RuntimeError("Training requires rgb_emb or rgb in cache, but neither is present.")
        elif "rgb" not in sample:
            raise RuntimeError("Current config trains RGB branch, but cache has no raw 'rgb'.")
    else:
        if bool(args.freeze_flow):
            if "flow_emb" not in sample and "flow" not in sample:
                raise RuntimeError("Training requires flow_emb or flow in cache, but neither is present.")
        elif "flow" not in sample:
            raise RuntimeError("Current config trains flow branch, but cache has no raw 'flow'.")

    if bool(args.freeze_audio):
        if "audio_emb" not in sample and "audio" not in sample:
            raise RuntimeError("Training requires audio_emb or audio in cache, but neither is present.")
    elif "audio" not in sample:
        raise RuntimeError("Current config trains audio branch, but cache has no raw 'audio'. Freeze audio or rebuild cache.")

    if bool(args.freeze_text) and text_policy != "drop" and cached_text_policy and cached_text_policy != text_policy:
        raise RuntimeError(
            f"Feature-cache text_policy mismatch: cache={cached_text_policy} current={text_policy}. "
            "Rebuild feature cache or switch --text-policy."
        )

    if not bool(args.zero_text):
        if not bool(args.freeze_text) and "mn" not in sample:
            raise RuntimeError("Current config trains text branch, but cache has no raw Mongolian text.")
        if bool(args.freeze_text) and ("text_emb" not in sample and "mn" not in sample):
            raise RuntimeError("Training requires text_emb or mn in cache, but neither is present.")


def set_seed(seed: int):
    """固定 Python / NumPy / PyTorch 随机种子。"""

    import random
    import numpy as np

    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)


def _resolve_ablation_flags(args: argparse.Namespace) -> tuple[bool, bool, bool]:
    """把 `--ablation` 预设和显式 zero-* 标志合并成最终消融开关。"""

    ablation = str(getattr(args, "ablation", "full") or "full").strip().lower()
    zero_video = bool(getattr(args, "zero_video", False))
    zero_audio = bool(getattr(args, "zero_audio", False))
    zero_text = bool(getattr(args, "zero_text", False))
    presets = {
        "full": (False, False, False),
        "text-only": (True, True, False),
        "audio-only": (True, False, True),
        "video-only": (False, True, True),
        "no-text": (False, False, True),
        "no-audio": (False, True, False),
        "no-video": (True, False, False),
    }
    preset = presets.get(ablation)
    if preset is not None:
        zero_video = zero_video or preset[0]
        zero_audio = zero_audio or preset[1]
        zero_text = zero_text or preset[2]
    return zero_video, zero_audio, zero_text


def _macro_f1(y_true: torch.Tensor, y_pred: torch.Tensor, num_classes: int) -> float:
    """从标签张量直接计算 macro-F1。"""

    # y_true/y_pred: (N,) int64
    if y_true.numel() == 0:
        return 0.0
    y_true = y_true.to(torch.long)
    y_pred = y_pred.to(torch.long)

    f1_sum = 0.0
    for c in range(int(num_classes)):
        tp = int(((y_true == c) & (y_pred == c)).sum().item())
        fp = int(((y_true != c) & (y_pred == c)).sum().item())
        fn = int(((y_true == c) & (y_pred != c)).sum().item())
        denom = (2 * tp + fp + fn)
        f1_c = (2 * tp / denom) if denom > 0 else 0.0
        f1_sum += float(f1_c)
    return float(f1_sum / max(1, int(num_classes)))


def _ccc(x: torch.Tensor, y: torch.Tensor) -> float:
    """计算 Concordance Correlation Coefficient。

    公式为：
    `CCC = 2 * cov(x, y) / (var(x) + var(y) + (mean_x - mean_y)^2)`。

    它同时惩罚：
    - 相关性差；
    - 均值偏移；
    - 方差不一致。

    因此比单纯的 Pearson 更适合连续强度回归评估。
    """
    x = x.flatten().to(torch.float64)
    y = y.flatten().to(torch.float64)
    n = int(x.numel())
    if n < 2:
        return 0.0
    mx = float(x.mean().item())
    my = float(y.mean().item())
    vx = float(x.var(unbiased=False).item())
    vy = float(y.var(unbiased=False).item())
    cov = float(((x - mx) * (y - my)).mean().item())
    denom = vx + vy + (mx - my) ** 2
    if denom <= 0:
        return 0.0
    return float((2.0 * cov) / denom)


def _to_jsonable(value: Any) -> Any:
    """把 Path/tuple 等对象递归转换成可写入 JSON 的结构。"""

    if isinstance(value, Path):
        return str(value)
    if isinstance(value, dict):
        return {str(k): _to_jsonable(v) for k, v in value.items()}
    if isinstance(value, (list, tuple)):
        return [_to_jsonable(v) for v in value]
    return value


def _save_metrics_and_plots(out_dir: Path, metrics: dict[str, Any]) -> None:
    """落盘训练指标，并尽量生成训练过程图表。"""

    out_dir.mkdir(parents=True, exist_ok=True)
    (out_dir / "metrics.json").write_text(json.dumps(metrics, ensure_ascii=False, indent=2), encoding="utf-8")

    # Plot (optional). If matplotlib isn't installed, we still keep metrics.json.
    try:
        import matplotlib

        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except Exception as e:
        print(f"Plot skipped (matplotlib not available): {type(e).__name__}: {e}")
        print("Install with: pip install matplotlib")
        return

    epochs = list(range(1, len(metrics.get("train_loss", [])) + 1))
    if not epochs:
        return

    label_names = list(
        metrics.get("meta", {}).get(
            "label_names",
            metrics.get("meta", {}).get("cache_config", {}).get("emotions", EMOTIONS),
        )
    )

    # Accuracy plot
    plt.figure(figsize=(8, 4.5))
    if "train_acc" in metrics:
        plt.plot(epochs, metrics["train_acc"], label="train")
    if "val_acc" in metrics:
        plt.plot(epochs, metrics["val_acc"], label="val")
    plt.xlabel("epoch")
    plt.ylabel("accuracy")
    plt.title("Accuracy vs Epoch")
    plt.grid(True, alpha=0.3)
    plt.legend()
    plt.tight_layout()
    plt.savefig(out_dir / "accuracy_curve.png", dpi=160)
    plt.close()

    # Loss plot
    plt.figure(figsize=(8, 4.5))
    if "train_loss" in metrics:
        plt.plot(epochs, metrics["train_loss"], label="train")
    if "val_loss" in metrics:
        plt.plot(epochs, metrics["val_loss"], label="val")
    plt.xlabel("epoch")
    plt.ylabel("loss")
    plt.title("Loss vs Epoch")
    plt.grid(True, alpha=0.3)
    plt.legend()
    plt.tight_layout()
    plt.savefig(out_dir / "loss_curve.png", dpi=160)
    plt.close()

    # Macro-F1 plot
    plt.figure(figsize=(8, 4.5))
    if "train_f1" in metrics:
        plt.plot(epochs, metrics["train_f1"], label="train")
    if "val_f1" in metrics:
        plt.plot(epochs, metrics["val_f1"], label="val")
    plt.xlabel("epoch")
    plt.ylabel("macro_f1")
    plt.title("Macro-F1 vs Epoch")
    plt.grid(True, alpha=0.3)
    plt.legend()
    plt.tight_layout()
    plt.savefig(out_dir / "macro_f1_curve.png", dpi=160)
    plt.close()

    # Per-class recall curves
    train_recall_history = metrics.get("train_per_class_recall", [])
    val_recall_history = metrics.get("val_per_class_recall", [])
    if isinstance(train_recall_history, list) and isinstance(val_recall_history, list) and (train_recall_history or val_recall_history):
        fig, axes = plt.subplots(2, 1, figsize=(10, 8), sharex=True)
        plot_specs = [
            (axes[0], train_recall_history, "Train Per-Class Recall"),
            (axes[1], val_recall_history, "Val Per-Class Recall"),
        ]
        for ax, recall_history, title in plot_specs:
            if not recall_history:
                ax.set_title(f"{title} (empty)")
                ax.grid(True, alpha=0.3)
                continue
            for label_name in label_names:
                values = [float(step.get(label_name, 0.0)) for step in recall_history]
                ax.plot(epochs[: len(values)], values, label=label_name)
            ax.set_ylabel("recall")
            ax.set_title(title)
            ax.set_ylim(0.0, 1.05)
            ax.grid(True, alpha=0.3)
        axes[-1].set_xlabel("epoch")
        handles, labels = axes[0].get_legend_handles_labels()
        if handles:
            fig.legend(handles, labels, loc="upper center", ncol=min(4, max(1, len(labels))), frameon=False)
        fig.tight_layout(rect=(0, 0, 1, 0.95))
        fig.savefig(out_dir / "per_class_recall_curve.png", dpi=160)
        plt.close(fig)

    # Best-val confusion matrix heatmap
    best_val_summary = metrics.get("best", {}).get("best_val_summary", {})
    matrix = best_val_summary.get("confusion_matrix", [])
    if isinstance(matrix, list) and matrix:
        plt.figure(figsize=(8.5, 7))
        ax = plt.gca()
        im = ax.imshow(matrix, cmap="Blues")
        plt.colorbar(im, ax=ax, fraction=0.046, pad=0.04)
        ax.set_title("Best Val Confusion Matrix")
        ax.set_xlabel("pred")
        ax.set_ylabel("true")
        ax.set_xticks(range(len(label_names)))
        ax.set_yticks(range(len(label_names)))
        ax.set_xticklabels(label_names, rotation=45, ha="right")
        ax.set_yticklabels(label_names)
        vmax = max((max(row) for row in matrix if row), default=0)
        threshold = vmax / 2.0 if vmax > 0 else 0.0
        for i, row in enumerate(matrix):
            for j, value in enumerate(row):
                ax.text(
                    j,
                    i,
                    str(int(value)),
                    ha="center",
                    va="center",
                    color="white" if float(value) > threshold else "black",
                    fontsize=8,
                )
        plt.tight_layout()
